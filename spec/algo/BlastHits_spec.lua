describe("npge.algo.BlastHits", function()
    it("finds hits using blast+", function()
        local Sequence = require 'npge.model.Sequence'
        local s1 = Sequence('s1', string.rep('ATGC', 100))
        local s2 = Sequence('s2', string.rep('ATGC', 100))
        local BlockSet = require 'npge.model.BlockSet'
        local bs_with_seqs = BlockSet({s1, s2}, {})
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(bs_with_seqs, bs_with_seqs)
        assert.truthy(#hits:blocks() > 0)
    end)

    it("finds hits using blast+ (bank is another blockset)",
    function()
        local Sequence = require 'npge.model.Sequence'
        local s1 = Sequence('s1', string.rep('ATGC', 100))
        local s2 = Sequence('s2', string.rep('ATGC', 100))
        local BlockSet = require 'npge.model.BlockSet'
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(BlockSet({s1}, {}),
            BlockSet({s2}, {}))
        assert.truthy(#hits:blocks() > 0)
    end)

    it("finds blast+ hits (another bank, shared sequence)",
    function()
        local Sequence = require 'npge.model.Sequence'
        local s1 = Sequence('s1', string.rep('ATGC', 100))
        local s2 = Sequence('s2', string.rep('ATGC', 100))
        local s3 = Sequence('s3', string.rep('ATGC', 100))
        local BlockSet = require 'npge.model.BlockSet'
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(BlockSet({s1, s2}, {}),
            BlockSet({s2, s3}, {}))
        assert.truthy(#hits:blocks() > 0)
    end)

    it("throws an error on sequence #names_conflist",
    function()
        local Sequence = require 'npge.model.Sequence'
        local s1a = Sequence('s1', string.rep('ATGC', 100))
        local s1b = Sequence('s1', string.rep('ATGC', 100))
        local BlockSet = require 'npge.model.BlockSet'
        local BlastHits = require 'npge.algo.BlastHits'
        assert.has_error(function()
            local hits = BlastHits(BlockSet({s1a}, {}),
                BlockSet({s1b}, {}))
        end)
    end)

    it("finds hits using blast+ (empty bank)",
    function()
        local Sequence = require 'npge.model.Sequence'
        local s1 = Sequence('s1', string.rep('ATGC', 100))
        local s2 = Sequence('s2', string.rep('ATGC', 100))
        local BlockSet = require 'npge.model.BlockSet'
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(BlockSet({s1, s2}, {}),
            BlockSet({}, {}))
        assert.equal(#hits:blocks(), 0)
    end)

    it("works if set of sequences is empty", function()
        local BlockSet = require 'npge.model.BlockSet'
        local bs = BlockSet({}, {})
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(bs, bs)
        assert.equal(hits, bs)
    end)

    it("works if sequence with description", function()
        local Sequence = require 'npge.model.Sequence'
        local s1 = Sequence('s1', string.rep('ATGC', 100),
            "ac=123 bg=4456 ttt yyy")
        local s2 = Sequence('s2', string.rep('ATGC', 100),
            "test")
        local BlockSet = require 'npge.model.BlockSet'
        local bs_with_seqs = BlockSet({s1, s2}, {})
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(bs_with_seqs, bs_with_seqs)
        assert.truthy(#hits:blocks() > 0)
    end)

    it("finds hits using blast+ (set evalue)", function()
        local Sequence = require 'npge.model.Sequence'
        local s1 = Sequence('s1', string.rep('ATGC', 100))
        local s2 = Sequence('s2', string.rep('ATGC', 100))
        local BlockSet = require 'npge.model.BlockSet'
        local bs_with_seqs = BlockSet({s1, s2}, {})
        local BlastHits = require 'npge.algo.BlastHits'
        local hits = BlastHits(bs_with_seqs, bs_with_seqs,
            {evalue=0.01})
        assert.truthy(#hits:blocks() > 0)
    end)

